package owlsogul.blog.me.stopdrinking.list;

public class ItemFirstFeedback {

    private int sleepHour, tension, drinkingYesterday, amountDrink, drinkness;


    public int getSleepHour() {
        return sleepHour;
    }

    public void setSleepHour(int sleepHour) {
        this.sleepHour = sleepHour;
    }

    public int getTension() {
        return tension;
    }

    public void setTension(int tension) {
        this.tension = tension;
    }

    public int getDrinkingYesterday() {
        return drinkingYesterday;
    }

    public void setDrinkingYesterday(int drinkingYesterday) {
        this.drinkingYesterday = drinkingYesterday;
    }

    public int getAmountDrink() {
        return amountDrink;
    }

    public void setAmountDrink(int amountDrink) {
        this.amountDrink = amountDrink;
    }

    public int getDrinkness() {
        return drinkness;
    }

    public void setDrinkness(int drinkness) {
        this.drinkness = drinkness;
    }
}
